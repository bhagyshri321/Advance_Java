package in.co.markseet;

public class MarksheetBean {
	
	private int ID;
	private int ROLLNO;
	private String FNAME ;
	private String LNAME;
	private int PHY;
	private int CHEM;
	private int MATHS;
	
	public MarksheetBean() {}

	public int getID() {
		return ID;
	}

	public void setID(int iD) {
		ID = iD;
	}

	public int getROLLNO() {
		return ROLLNO;
	}

	public void setROLLNO(int i) {
		ROLLNO = i;
	}

	public String getFNAME() {
		return FNAME;
	}

	public void setFNAME(String fNAME) {
		FNAME = fNAME;
	}

	public String getLNAME() {
		return LNAME;
	}

	public void setLNAME(String lNAME) {
		LNAME = lNAME;
	}

	public int getPHY() {
		return PHY;
	}

	public void setPHY(int pHY) {
		PHY = pHY;
	}

	public int getCHEM() {
		return CHEM;
	}

	public void setCHEM(int cHEM) {
		CHEM = cHEM;
	}

	public int getMATHS() {
		return MATHS;
	}

	public void setMATHS(int mATHS) {
		MATHS = mATHS;
	}
	
	

}
	
